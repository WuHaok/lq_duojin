function jiaZai(){
    for(var i=0;i<jsonlink.data.length;i++)
    {
        if(jsonlink.data[i][0].provinceCode.length==2){
            provinceCodeArrSheng.push(jsonlink.data[i][0].provinceCode)
            provinceNameSheng.push(jsonlink.data[i][0].provinceName)
        }
        if(jsonlink.data[i][0].provinceCode.length==4){
            provinceCodeArrShi.push(jsonlink.data[i][0].provinceCode)
            provinceNameShi.push(jsonlink.data[i][0].provinceName)
        }
        if(jsonlink.data[i][0].provinceCode.length==6){
            provinceCodeArrXian.push(jsonlink.data[i][0].provinceCode)
            provinceNameXian.push(jsonlink.data[i][0].provinceName)
        }

    }
    xianShi();
}
//页面加载完成后，显示省级城市
function xianShi()
{
    for(var i=0;i<provinceCodeArrSheng.length;i++)
    {
        var optionObj=document.createElement("option");
        optionObj.value=provinceCodeArrSheng[i]+" "+provinceNameSheng[i];
        optionObj.innerHTML=provinceNameSheng[i];
        $("#shengJi").append(optionObj);
    }
    xianShiJi();
}
